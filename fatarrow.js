/**
 * FatArrow Function to return the name and age of an employee
 * @param name - string
 * @param age - number
 */
var personDetails = function (name, age) { return 'The Person Name is ' + name + ' who is ' + age + ' years old.'; };
// Calling the Person Details and storing into a result Variable 
var result = personDetails('Arvind', 44);
// Result will be printed in the Console
console.log(result);
